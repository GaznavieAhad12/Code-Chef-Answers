-- Trigger timing
-- Trigger timing refers to when a trigger fires in relation to the triggering event.

-- BEFORE Triggers
-- BEFORE triggers are executed before the triggering event takes place.
-- They are useful for performing actions or modifications to the data before it is inserted, updated, or deleted from the table.
-- Common use cases for BEFORE triggers include data validation, default value assignment, or data modification based on certain conditions.

-- AFTER Triggers
-- AFTER triggers are executed after the triggering event has occurred.
-- They are typically used for validation, auditing, logging, or other post-processing tasks.
-- Common use cases for AFTER triggers include logging changes, updating related tables, or performing calculations based on the changes made by the triggering event.











