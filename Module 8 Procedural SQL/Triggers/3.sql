-- Trigger events
-- Trigger events define the specific database operations that can cause a trigger to fire (i.e., execute).
-- In PL/SQL, triggers can be associated with three main types of events: INSERT, UPDATE, and DELETE.

-- Use cases for each event

-- UPDATE
-- Validating or enforcing constraints on updated data.
-- Logging changes made to records.
-- Triggering additional actions based on updates.
-- INSERT / DELETE
-- Cascading deletions to related tables.
-- Auditing or logging activities
-- Enforcing business rules for new data / data to be deleted
-- Check some such rules built in in the triggers populated in the IDE.