-- Case study - Logistics
-- The 'truck' table has the following information.

-- truck_id	cost_per_distance	truck_type
-- Truck1	1.2	Small
-- Truck2	0.9	Medium
-- Truck3	0.8	Large
-- Trailer1	0.7	Trailer


Correct Answer:

Larger trucks can carry more 'tonnage' / weight of goods.
The fixed cost of operating a truck - driver expenses, fuel, toll gets distributed and hence the cost / distance is lower.